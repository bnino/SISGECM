    private void buscar_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscar_btnActionPerformed
        if(busqueda_txt.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "POR FAVOR INGRESE DATO DE BÚSQUEDA","ERROR",JOptionPane.ERROR_MESSAGE);
            busqueda_txt.requestFocus();
        }else if(!busqueda_txt.getText().matches("\\d*"))
        {
            JOptionPane.showMessageDialog(null, "LA BÚSQUEDA SOLO PUEDE CONTENER NÚMEROS","ERROR",JOptionPane.ERROR_MESSAGE);
        }else
        {
            buscaPaciente(busqueda_txt.getText());
        }
    }//GEN-LAST:event_buscar_btnActionPerformed

    private void busca_horario_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_busca_horario_btnActionPerformed
        this.hora_slc.removeAllItems();
        String fecha = dia_txt.getCalendar().get(Calendar.YEAR)+"-";
        fecha += (dia_txt.getCalendar().get(Calendar.MONTH)+1)+"-";
        fecha+=dia_txt.getCalendar().get(Calendar.DAY_OF_MONTH);
        horarioDisponible(fecha, idmedico);
    }//GEN-LAST:event_busca_horario_btnActionPerformed

    private void modificar_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificar_btnActionPerformed
        String fecha = dia_txt.getCalendar().get(Calendar.YEAR)+"-";
        fecha += (dia_txt.getCalendar().get(Calendar.MONTH)+1)+"-";
        fecha+=dia_txt.getCalendar().get(Calendar.DAY_OF_MONTH);
        boolean respuesta = Consultas.modificaCita(Integer.parseInt(busqueda_txt.getText()),fecha , nueva_hora_txt.getText(),idmedico);
        
        if(respuesta)
        {
            busqueda_txt.setText("");
            busqueda_txt.requestFocus();
            paciente_txt.setText("");
            medico_txt.setText("");
            dia_txt.setDate(null);
            hora_actual_txt.setText("");
            nueva_hora_txt.setText("");
            hora_slc.removeAllItems();
            this.cambiar_med_btn.setEnabled(false);
            this.busca_horario_btn.setEnabled(false);
            this.modificar_btn.setEnabled(false);
            idmedico=0;
            
            JOptionPane.showMessageDialog(null, "CITA ACTUALIZADA","INFORMACIÓN",JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "OCURRIÓ UN ERROR AL ACTUALZIAR LA CITA","ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_modificar_btnActionPerformed

