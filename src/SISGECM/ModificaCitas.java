    public static class getHorarioCita{
        
        public static void recibeHorarioCita(int idmed,String medico, String dia, String hora){
            Date nuevoDia = Date.valueOf(dia);
            nueva_hora_txt.setText("");
            idmedico = idmed;
            medico_txt.setText(medico);
            dia_txt.setDate(nuevoDia);
            nueva_hora_txt.setText(hora);
            hora_slc.removeAllItems();
            horarioDisponible(dia, idmedico);
        }
    }
    
    public void buscaPaciente(String doc){
        try {
            Connection conexion= c.getConexion();
            
            PreparedStatement sentencia = conexion.prepareStatement("SELECT pacientes.doc, CONCAT(pacientes.nombre1,' ', pacientes.nombre2,' ', pacientes.apellido1,' ',"+
                    " pacientes.apellido2) AS paciente, CONCAT(medicos.nombre,' ', medicos.apellidos) AS medico,medicos.idmedicos, DATE_FORMAT(citas.hora, '%h: %i %p') AS hora, citas.dia " +
                    "FROM pacientes, medicos, citas " +
                    "WHERE citas.pacientes_docpaciente=? " +
                    "AND pacientes.doc = citas.pacientes_docpaciente " +
                    "AND medicos.idmedicos = citas.medicos_idmedicos " +
                    "AND citas.estado='ASIGNADA'");
            
            sentencia.setString(1, doc);
            
            ResultSet rs = sentencia.executeQuery();
            if(rs.next()){
                this.paciente_txt.setText(rs.getString("paciente"));
                
                this.medico_txt.setText(rs.getString("medico"));
                
                java.util.Date fechaCita = Date.valueOf(rs.getString("dia"));
                this.dia_txt.setDate(fechaCita);
                idmedico=Integer.parseInt(rs.getString("idmedicos"));
                String fecha = dia_txt.getCalendar().get(Calendar.YEAR)+"-";
                fecha += (dia_txt.getCalendar().get(Calendar.MONTH)+1)+"-";
                fecha+=dia_txt.getCalendar().get(Calendar.DAY_OF_MONTH);
                this.hora_actual_txt.setText(rs.getString("hora"));
                this.cambiar_med_btn.setEnabled(true);
                this.busca_horario_btn.setEnabled(true);
                this.modificar_btn.setEnabled(true);
                
                horarioDisponible(fecha, idmedico);
            }
            else{
                JOptionPane.showMessageDialog(null, "LA BÚSQUEDA NO ARROJÓ RESULTADOS, VERIFIQUE");
                this.busqueda_txt.requestFocus();
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }
    
    public static void horarioDisponible(String fecha, int idmed){
        ArrayList<String> horarios = Consultas.selecciona_horario(fecha, idmed);
        
        for(int i=0; i<horarios.size();i++){
            hora_slc.addItem(horarios.get(i));
        }
    }
